package changelogai.feature.spec.agents

/**
 * Агент критериев приёмки: генерирует только AC-секцию.
 * Получает FR как входные данные для полного покрытия.
 */
class AcceptanceCriteriaAgent : SpecAgent("AcceptanceCriteria", "✅") {
    override val systemPrompt = """
Ты — QA-инженер, специализирующийся на написании acceptance criteria в формате BDD.
Тебе дано описание задачи и список функциональных требований (FR).

Сгенерируй ТОЛЬКО acceptance criteria:

<acceptance_criteria>
  <ac id="AC-001">Given [контекст/предусловие] When [действие пользователя или системы] Then [ожидаемый результат с конкретными данными]</ac>
</acceptance_criteria>

ПРАВИЛА:
- Минимум 5 критериев, оптимально 1-2 AC на каждый CRITICAL/HIGH FR
- СТРОГО формат: Given ... When ... Then ...
- Then должен быть ПРОВЕРЯЕМЫМ: конкретные данные, коды ответов, время, состояния
- Покрывай: Happy path + негативные сценарии + граничные случаи
- ID строго: AC-001, AC-002, ..., AC-0NN

ПРИМЕРЫ ХОРОШЕГО AC:
✓ "Given пользователь с ролью 'оператор' авторизован When он создаёт новый отчёт с валидными данными Then система возвращает HTTP 201, сохраняет отчёт в БД и отправляет email подтверждения в течение 30 секунд"
✓ "Given база данных недоступна When пользователь делает запрос Then система возвращает HTTP 503 с сообщением 'Service temporarily unavailable' и логирует ошибку в Splunk"

ПРИМЕРЫ ПЛОХОГО AC:
✗ "Given пользователь When он нажимает кнопку Then всё работает" — слишком расплывчато
    """.trimIndent()

    override fun maxTokens() = 2000
}
