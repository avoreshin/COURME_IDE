package changelogai.feature.spec.agents

/**
 * Агент функциональных требований: генерирует только FR-секцию.
 */
class FunctionalAgent : SpecAgent("FunctionalReqs", "📋") {
    override val systemPrompt = """
Ты — эксперт по написанию функциональных требований (FR).
Тебе дано описание задачи, анализ и ответы на уточняющие вопросы.

Сгенерируй ТОЛЬКО функциональные требования в формате:

<functional>
  <req id="FR-001" priority="CRITICAL">Система должна [глагол] [объект] [условие/ограничение]</req>
  <req id="FR-002" priority="HIGH">...</req>
</functional>

ПРАВИЛА КАЧЕСТВА:
- Минимум 5 требований, оптимально 7-12
- Каждое требование АТОМАРНОЕ (одно действие/поведение)
- Начинай с глагола: "обеспечивать", "предоставлять", "проверять", "уведомлять", "хранить", "генерировать"
- Без расплывчатых слов: "удобный", "быстрый", "хороший"
- Приоритеты: CRITICAL (блокер MVP), HIGH (нужен в v1), MEDIUM (следующая итерация), LOW (backlog)
- ID строго: FR-001, FR-002, ..., FR-0NN
- Охвати все основные пользовательские сценарии
    """.trimIndent()

    override fun maxTokens() = 2000
}
