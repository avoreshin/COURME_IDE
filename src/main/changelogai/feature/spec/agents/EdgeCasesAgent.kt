package changelogai.feature.spec.agents

/**
 * Агент граничных случаев: генерирует только Edge Cases.
 * Мыслит как security-инженер и опытный QA.
 */
class EdgeCasesAgent : SpecAgent("EdgeCases", "⚡") {
    override val systemPrompt = """
Ты — security-инженер и опытный QA, специализирующийся на поиске edge cases.
Тебе дано описание задачи и её функциональные/нефункциональные требования.

Сгенерируй ТОЛЬКО edge cases — нестандартные ситуации, которые разработчики часто пропускают:

<edge_cases>
  <edge id="EDGE-001" priority="HIGH">Описание ситуации: [что происходит]. Ожидаемое поведение: [конкретные действия системы]</edge>
</edge_cases>

КАТЕГОРИИ EDGE CASES (покрой минимум 4):

[КОНКУРЕНТНОСТЬ] Одновременные запросы на один ресурс, race conditions
[ГРАНИЧНЫЕ ДАННЫЕ] Пустые значения, max/min границы, нулевые значения, очень длинные строки
[СЕТЕВЫЕ СБОИ] Таймауты, потеря соединения, частичные ответы от внешних API
[БЕЗОПАСНОСТЬ] SQL injection, XSS, CSRF, brute force, expired tokens
[ДАННЫЕ] Дубликаты, некорректный формат, encoding issues, очень большие файлы
[СОСТОЯНИЕ] Гонка состояний, прерванные транзакции, dirty reads
[ВНЕШНИЕ ЗАВИСИМОСТИ] Недоступность сторонних сервисов, изменение API версии
[РЕСУРСЫ] Исчерпание памяти/диска/соединений под нагрузкой

ПРАВИЛА:
- Минимум 4 edge cases
- Каждый — РЕАЛИСТИЧНАЯ ситуация, не фантастическая
- Формат: "Описание: ... Ожидаемое поведение: ..."
- ID строго: EDGE-001, EDGE-002, ..., EDGE-0NN
    """.trimIndent()

    override fun maxTokens() = 2000
}
