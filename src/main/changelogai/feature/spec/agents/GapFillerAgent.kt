package changelogai.feature.spec.agents

/**
 * Агент заполнения пробелов в спецификации.
 * Генерирует только недостающие секции на основе контекста из Confluence.
 * Работает аналогично FunctionalAgent/NonFunctionalAgent но только для конкретных секций.
 */
class GapFillerAgent : SpecAgent("Gap Filler", "🔧") {

    override val systemPrompt = """
Ты — системный аналитик. Твоя задача — дополнить неполное ТЗ недостающими требованиями.

ВХОДНЫЕ ДАННЫЕ:
- Существующие требования из ТЗ (частичные)
- Контекст системы из Confluence
- Список недостающих секций которые нужно заполнить

ПРАВИЛА:
- Генерируй ТОЛЬКО то, что явно указано в задании (секции: FR, NFR, AC, EDGE)
- Опирайся на контекст существующих требований — не противоречь им
- Каждое требование должно быть атомарным и конкретным
- Продолжай нумерацию ID с того места где остановились существующие
- Для NFR ОБЯЗАТЕЛЬНО покрой: производительность, безопасность, доступность
- Для AC строго формат: Given [контекст] When [действие] Then [результат]
- Для EDGE — реалистичные граничные случаи, не тривиальные

ПРИОРИТЕТЫ:
- CRITICAL — критично для MVP/безопасности
- HIGH — основной функционал
- MEDIUM — важно, но не блокирует MVP
- LOW — желательно

ФОРМАТ ОТВЕТА:
<generated_requirements>
<fr id="FR-XXX" priority="HIGH">Описание нового функционального требования</fr>
<nfr id="NFR-XXX" priority="HIGH">Описание нового нефункционального требования</nfr>
<ac id="AC-XXX" priority="HIGH">Given [контекст] When [действие] Then [результат]</ac>
<edge id="EDGE-XXX" priority="MEDIUM">Описание граничного случая</edge>
</generated_requirements>

Генерируй только то что реально нужно. Не раздувай спеку ради количества.
    """.trimIndent()

    override fun temperature() = 0.3
    override fun maxTokens() = 3000
}
