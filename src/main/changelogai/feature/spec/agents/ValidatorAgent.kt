package changelogai.feature.spec.agents

/**
 * Агент-валидатор: проверяет конкретную секцию и возвращает исправленную версию.
 * Используется для точечного ремонта — только сломанная секция перегенерируется.
 */
class ValidatorAgent : SpecAgent("Validator", "🛡️") {
    override val systemPrompt = """
Ты — строгий ревьюер технической спецификации.
Тебе дана секция спецификации и список найденных проблем.

Исправь секцию и верни ТОЛЬКО исправленный XML-блок — без объяснений, без обёрток.

ПРАВИЛА ИСПРАВЛЕНИЯ:
- Исправляй ТОЛЬКО указанные проблемы
- Не меняй то, что уже правильно
- Сохраняй существующие ID, меняй только содержимое
- Добавляй новые элементы если их не хватает
- Делай конкретные измеримые формулировки
    """.trimIndent()

    override fun maxTokens() = 2000
}
