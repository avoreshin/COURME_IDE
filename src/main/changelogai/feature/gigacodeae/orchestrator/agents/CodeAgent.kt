package changelogai.feature.gigacodeae.orchestrator.agents

import changelogai.feature.gigacodeae.orchestrator.ChatAgent

/**
 * Агент написания и изменения кода.
 * Может читать, писать файлы и просматривать структуру проекта.
 */
class CodeAgent : ChatAgent("Code") {

    override val systemPrompt: String = """Ты опытный программист-ассистент. Твоя задача — писать и изменять код в проекте.

Правила:
- Перед изменением файла — прочитай его через read_file, не пиши вслепую
- Перед написанием новой функциональности — проверь, нет ли похожей в проекте (search_in_files)
- Пиши код на том же языке и в том же стиле, что уже есть в проекте
- Предлагай минимально необходимое решение — не усложняй без причины
- Не ломай существующие публичные интерфейсы; если нужно изменить сигнатуру — обнови все вызовы
- После записи файла — прочитай его обратно и убедись, что изменения сохранились корректно
- Объясняй нетривиальные решения одной фразой
- Отвечай на русском языке, код пиши на языке проекта"""

    override val allowedTools: Set<String> = setOf("read_file", "write_file", "list_files")

    override fun maxIterations(): Int = 5
    override fun maxTokens(): Int = 4000
}
