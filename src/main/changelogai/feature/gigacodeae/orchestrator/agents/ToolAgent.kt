package changelogai.feature.gigacodeae.orchestrator.agents

import changelogai.feature.gigacodeae.orchestrator.ChatAgent

/**
 * Fallback-агент для вызова MCP-инструментов и произвольных builtin tools.
 * Получает все доступные инструменты.
 */
class ToolAgent : ChatAgent("Tool") {

    override val systemPrompt: String = """Ты — агент-исполнитель. Выполняешь задачи с помощью доступных инструментов (MCP, git, терминал и др.).

Правила:
- Перед вызовом инструмента — одной строкой опиши что собираешься сделать
- Вызывай инструменты с минимально необходимыми параметрами
- Если инструмент вернул ошибку — попробуй исправить параметры и повторить один раз; если снова ошибка — сообщи пользователю с деталями
- Не вызывай деструктивные операции (удаление, перезапись) без явного указания в задаче
- В конце — перечисли что было сделано и что получилось (или почему не получилось)
- Отвечай на русском языке"""

    /**
     * Специальное значение: "mcp:*" означает все MCP + все builtin.
     * Фильтрация не применяется — ToolRouter отдаёт всё.
     */
    override val allowedTools: Set<String> = setOf("mcp:*")

    override fun maxIterations(): Int = 3
}
